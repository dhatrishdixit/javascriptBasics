const month = 3 ;

switch (month) {
    case 0:
        console.log("jan");        
        break;
    case 1:
        console.log("feb");        
        break;
    case 2:
        console.log("mar");        
        break;
    case 3:
        console.log("apr");        
        break;
    case 4:
        console.log("may");        
        break;
    case 5:
        console.log("jun");        
        break;

    default:
        console.log("default value");
        break;
}

